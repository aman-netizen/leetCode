class Solution
{
    public:
    Node *compute(Node *head)
    {
        stack<int> st;
        Node* curr=head;
        while(curr!=NULL){
            while(!st.empty() && st.top()<curr->data){
                st.pop();
            }
            st.push(curr->data);
            curr=curr->next;
        }
        Node* ans=new Node(0);
        Node* nexxt=ans->next;
        while(!st.empty()){
            Node* curr=new Node(st.top());
            ans->next=curr;
            curr->next=nexxt;
            nexxt=curr;
            st.pop();
        }
        return ans->next;
    }
    
};
   


