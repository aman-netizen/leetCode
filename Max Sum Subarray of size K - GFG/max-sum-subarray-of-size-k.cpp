class Solution{   
public:
    long maximumSumSubarray(int K, vector<int> &Arr , int N){
        // code here 
        long long temp=0,ans;
        for(int i=0;i<K;i++){
            temp+=Arr[i];
        }
        ans=temp;
        int s=0;
        int e=K;
        while(e<N){
            temp = temp-Arr[s]+Arr[e];
            s++;
            e++;
            ans=max(ans,temp);
        }
        return ans;
    }
};

